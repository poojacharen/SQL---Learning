-- Storage Engines
-- In MySQL we have several storage engines and in these storage engines determine how the data is stored and what features are available to us.
-- The 2 most commonly used storage engines are MyISAM and InnoDB
-- MyISAM is an older storage engine, it is used to be popular prior to version 5.5
-- But going forward InnoDB is the superior storage engine, it supports features like transactions, etc... MyISAM doesn't support these features

